namespace IGK.PDFApi
{
    public class PDFStringData : PDFDataValueBase 
    {
        public PDFStringData(string value):base(){ 
            this.Value = value ;
    }
        public static implicit operator PDFStringData (string data)
        {
            return new PDFStringData (data);
        }
        public override string ToString()
        {
            return this.Value;
        }
        public override string ToPdf()
        {
            return this.Value ;
        }
    }
